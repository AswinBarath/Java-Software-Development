package java_basics;

public class VariablesContinued {

	public static void main(String[] args) {
		int var = 1000000;
		long bigNumber1 = 1000000000000L;
		long bigNumber2 = -10000000000000L;
		short shortNumber1 = 32767;
		short shortNumber2 = -32768;
		byte reallySmallNumber1 = 127;
		byte reallySmallNumber2 = -128;
		double decimalVariable = 399.00;
		boolean decision = true;
		char letter = 'a';
		
		System.out.println(var);
		System.out.println(bigNumber1 + bigNumber2);
		System.out.println(shortNumber1);
		System.out.println(shortNumber2);
		System.out.println(reallySmallNumber1);
		System.out.println(reallySmallNumber2);
		System.out.println(decimalVariable);
		System.out.println(decision);
		System.out.println(letter);
		
	}

}